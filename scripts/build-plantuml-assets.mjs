/**
 * Copies @plantuml/core assets (plantuml.js, viz-global.js) into
 * extension/media/plantuml/ and generates the thin plantuml-client.js
 * glue module that powers the PlantUML webview preview.
 *
 * The media/ tree is gitignored build output; this script is part of
 * the `extension:prep` pipeline that runs before VSIX packaging.
 */
import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const coreDir = path.join(root, 'node_modules', '@plantuml', 'core');
const outDir = path.join(root, 'extension', 'media', 'plantuml');

await fs.mkdir(outDir, { recursive: true });

// Copy engine files
await fs.copyFile(path.join(coreDir, 'plantuml.js'), path.join(outDir, 'plantuml.js'));
await fs.copyFile(path.join(coreDir, 'viz-global.js'), path.join(outDir, 'viz-global.js'));

// Generate the thin ES-module client.
// Loaded as <script type="module" src="plantuml-client.js">; its static
// import of './plantuml.js' resolves relative to its own vscode-resource://
// URI, so both files must live in the same directory.
const clientSrc = `// Auto-generated by scripts/build-plantuml-assets.mjs — do not edit.
// ES module glue that bridges the VS Code postMessage channel and
// the @plantuml/core renderToString API.
import { renderToString } from './plantuml.js';

const vscode = acquireVsCodeApi();

function humanizeError(raw) {
  const msg = typeof raw === 'string' ? raw : String(raw ?? '');
  if (/cannot find.*include/i.test(msg) || /cannot find resource/i.test(msg)) {
    return {
      title: 'Include file not found',
      hint: 'Check that the !include path is correct relative to this diagram.',
    };
  }
  const lineMatch = /line (\\d+)/i.exec(msg);
  if (/syntax error/i.test(msg) || /lexical error/i.test(msg)) {
    return {
      title: 'Syntax error' + (lineMatch ? ' on line ' + lineMatch[1] : ''),
      hint: 'Fix the PlantUML syntax near the indicated line.',
    };
  }
  if (/timeout/i.test(msg) || /too many/i.test(msg)) {
    return {
      title: 'Diagram is too large to render',
      hint: 'Reduce the number of elements, or split into separate diagrams.',
    };
  }
  if (/undefined/i.test(msg) && lineMatch) {
    return {
      title: 'Unknown identifier on line ' + lineMatch[1],
      hint: 'Check for typos in element names or missing stereotype definitions.',
    };
  }
  return { title: 'Diagram error', hint: '' };
}

// Tracks the most recently requested render. renderToString is async and the
// host can send a newer 'render' before an older one resolves (e.g. the user
// switches the tracked file mid-render) — a stale callback firing after a
// newer one would overwrite the canvas and the host's exported SVG with the
// wrong diagram, so late callbacks for a superseded seq are dropped.
let latestSeq = 0;

function renderDiagram(source, seq) {
  latestSeq = seq;
  const loadingEl = document.getElementById('puml-loading');
  const errorCard = document.getElementById('puml-error');
  const errorTitle = document.getElementById('puml-error-title');
  const errorBody = document.getElementById('puml-error-body');
  const errorHint = document.getElementById('puml-error-hint');
  const outputEl = document.getElementById('puml-output');

  outputEl.innerHTML = '';
  errorCard.style.display = 'none';
  loadingEl.style.display = 'block';

  renderToString(
    source.split('\\n'),
    (svg) => {
      if (seq !== latestSeq) return;
      outputEl.innerHTML = svg;
      loadingEl.style.display = 'none';
      // Push the rendered SVG back to the host so Save/Copy can read it
      // synchronously — rendering happens here in the webview (wasm engine),
      // never host-side, so this is the only way the host ever sees it.
      vscode.postMessage({ type: 'rendered', svg: svg, seq: seq });
    },
    (err) => {
      if (seq !== latestSeq) return;
      const { title, hint } = humanizeError(err);
      errorTitle.textContent = title;
      errorBody.textContent = typeof err === 'string' ? err : String(err ?? '');
      errorHint.textContent = hint;
      errorCard.style.display = 'block';
      loadingEl.style.display = 'none';
      // Clear the host's held SVG — the last successful render no longer
      // matches what's on screen (the error card replaced it).
      vscode.postMessage({ type: 'rendered', svg: '', seq: seq });
    },
  );
}

window.addEventListener('message', (event) => {
  const msg = event.data;
  if (msg?.type === 'render') {
    renderDiagram(msg.source, msg.seq);
  }
});

// Signal to the extension host that the engine is loaded and ready.
vscode.postMessage({ type: 'ready' });
`;

await fs.writeFile(path.join(outDir, 'plantuml-client.js'), clientSrc, 'utf8');

const plantumlKb = Math.round((await fs.stat(path.join(outDir, 'plantuml.js'))).size / 1024);
const vizKb = Math.round((await fs.stat(path.join(outDir, 'viz-global.js'))).size / 1024);
console.log(`PlantUML assets → extension/media/plantuml/ (plantuml.js ${plantumlKb} KB, viz-global.js ${vizKb} KB)`);
